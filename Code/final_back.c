#include <io.h>
#include <delay.h>


void display_step_1(void);

void main(void)
{
    unsigned char i=0;
    DDRB = 0xFF;
    DDRD = 0xFF;
    
    PORTB = 0;
    while (1){
        for (i; i < 7; i++)
        {
            PORTD = 1<<i;
            delay_ms(50);
        }
        for (i; i > 0; i--)
        {
            PORTD = 1<<i;
            delay_ms(50);
        }
    }
}

void display_step_1(void)
{
    unsigned char i = 0;

    for (i; i < 8; i++)
    {
        PORTD = 1<<i;
        PORTB = 0xFF;
        delay_ms(100);
    }
    for (i; i > 0; i--)
    {
        PORTD = 1<<i;
        PORTB = 0xFF;
        delay_ms(100);
    }
}
